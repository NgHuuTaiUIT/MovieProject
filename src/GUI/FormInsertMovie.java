    private void btnAcceptActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAcceptActionPerformed
        // TODO add your handling code here:
        if(!txtIDMovie.getText().isEmpty()
                && !txtMovieName.getText().isEmpty()
                && !txtMovieType.getText().isEmpty()
                && !txtTime.getText().isEmpty()
                && !txtAge.getText().isEmpty()){
            int mvID = Integer.parseInt(txtIDMovie.getText());
            String mvName = txtMovieName.getText();
            String mvType = txtMovieType.getText();
            int duration = Integer.parseInt(txtTime.getText());
            String age = txtAge.getText();
            if(mvDTO.insertMovie(mvID,mvName,mvType,duration,age))
            {
                mvMovie.LoadDataMovie();
                mvMovie.setVisible(false);
                mvMovie.setVisible(true);
            }else
                new ErrorGUI("Insert Fail!");
            dispose();
        }else new ErrorGUI("Xin nhập đầy đủ thông tin!");
    }//GEN-LAST:event_btnAcceptActionPerformed

