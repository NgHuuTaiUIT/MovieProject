    private void jComboBox1ItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_jComboBox1ItemStateChanged
        // TODO add your handling code here:        
        if(txtMovieType.getText().equalsIgnoreCase("Input Text"))
            txtMovieType.setText("");
        
        if(!str.equalsIgnoreCase(jComboBox1.getSelectedItem().toString())){
                str=jComboBox1.getSelectedItem().toString();
            if(txtMovieType.getText().equals("")){
                txtMovieType.setText(str);
            }else
                txtMovieType.setText(txtMovieType.getText() + "," + str);
        }

        
    }//GEN-LAST:event_jComboBox1ItemStateChanged

