    public void LoadDataStaff(){
        tableModel = new DefaultTableModel();
        tblStaff.getTableHeader().setFont(new Font("Tahoma",Font.BOLD,18));
        tblStaff.getTableHeader().setOpaque(false);
        tblStaff.getTableHeader().setBackground(Color.red);
        tblStaff.getTableHeader().setForeground(new Color(255,255,255));
        String columnName[] = {"Staff ID","Name","Date of birth","User","Pass","Role"};
        tableModel.setColumnIdentifiers(columnName);
        tblStaff.setModel(tableModel);
        rowSorter = new TableRowSorter<>(tblStaff.getModel());
        tblStaff.setRowSorter(rowSorter);

        txttim.getDocument().addDocumentListener(new DocumentListener() {
            @Override
            public void insertUpdate(DocumentEvent e) {
                String text = txttim.getText();
                if (text.trim().length() == 0) {
                    rowSorter.setRowFilter(null);
                } else {
                    rowSorter.setRowFilter(RowFilter.regexFilter("(?i)" + text));
                }
            }

            @Override
            public void removeUpdate(DocumentEvent e) {
                String text = txttim.getText();
                if (text.trim().length() == 0) {
                    rowSorter.setRowFilter(null);
                } else {
                    rowSorter.setRowFilter(RowFilter.regexFilter("(?i)" + text));
                }
            }

            @Override
            public void changedUpdate(DocumentEvent e) {
            }
        });
        DBConnection con = new DBConnection();
        String sql = "Select * from Staff where role = 'Staff' order by id_staff";
        if(LOGIN1.emp_login.getEmp_role().equalsIgnoreCase("Admin"))
            sql = "Select * from Staff where role = 'Staff' or role = 'Manager' order by id_staff";

         ResultSet rs = con.getData(sql);
        
        try {
            while(rs.next()) {
                String value[] = new String[6];
                value[0] = rs.getString("ID_STAFF");
                value[1] = rs.getString("NAME_STAFF");
                value[2] = rs.getDate("DATE_bIRTH").toString();
                //Time timestart = new Time(rs.getTimestamp("TIME_START").getTime());
                //value[3] = timestart.toString().substring(0, 5);
                //Time timeend = new Time(rs.getTimestamp("TIME_END").getTime());
                //value[4] = timeend.toString().substring(0, 5);
                value[3] = rs.getString("USERNAME");
                value[4] = rs.getString("PASSWORD");
                value[5] = rs.getString("Role");
                tableModel.addRow(value);
            }
        } catch (SQLException ex) {
            Logger.getLogger(ShowTime.class.getName()).log(Level.SEVERE, null, ex);
        }
        con.CloseConnection();
    }

