    public void LoadDataTransaction(){
        tableModel = new DefaultTableModel();
        tblTransaction.getTableHeader().setFont(new Font("Tahoma",Font.BOLD,18));
        tblTransaction.getTableHeader().setOpaque(false);
        tblTransaction.getTableHeader().setBackground(Color.red);
        tblTransaction.getTableHeader().setForeground(new Color(255,255,255));
        String columnName[] = {"Bill","Staff","Customer","Movie","Date","Time","Total"};
        tableModel.setColumnIdentifiers(columnName);
        tblTransaction.setModel(tableModel);
        
        DBConnection con = new DBConnection();
        ResultSet rscus = con.getData("Select * from Customer");
        Vector<Customer_DTO> listCus = new Vector<>();
        try {
            while(rscus.next()){
                listCus.add(new Customer_DTO(rscus.getInt("IDcus"), rscus.getString("Name"), rscus.getInt("Member"),rscus.getInt("Point")));
            }
        } catch (SQLException ex) {
            Logger.getLogger(Transaction.class.getName()).log(Level.SEVERE, null, ex);
        }
        con.CloseConnection();
        String sql = "select b.*,mv.movie_name,sta.name_staff,st.time_start,st.time_end"
                + " from bill b,staff sta, movie mv, showtime st"
                + " where b.showtime_id = st.ID and"
                + " b.staff_id = sta.id_staff and"
                + " mv.id = st.movie_id order by b.bill_id";
        System.out.println(sql);
        
        DBConnection con2 = new DBConnection();
         ResultSet rsbill = con2.getData(sql);
        
        try {
            while(rsbill.next()) {
                String value[] = new String[7];
                value[0] = ""+rsbill.getInt("bill_ID");
                value[1] = rsbill.getString("name_staff");
                value[3] = rsbill.getString("Movie_Name");
                if(!listCus.isEmpty())
                {   for (Customer_DTO item : listCus) {
                        if(item.getId()==rsbill.getInt("cus_id"))
                        value[2] = item.getName();
                        break;
                    }
                    
                }else
                    value[2] = "null";
                value[4] = rsbill.getString("Date_bill");
                value[5] = rsbill.getString("time_start") + "-" + rsbill.getString("time_end");
                value[6] = ""+rsbill.getInt("price");
                tableModel.addRow(value);
            }
        } catch (SQLException ex) {

            Logger.getLogger(ShowTime.class.getName()).log(Level.SEVERE, null, ex);
        }
        con2.CloseConnection();
    }

